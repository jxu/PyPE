M = 123454321

def sum_pow10(t):
    # Calculates Sum_[k=0..t] (t-k+1) 10^(6k) mod M
    # Before mod M this is 1, 1 000002, 1 000002 000003, etc.
    # Mathematica: (-1999999 + 10^(12 + 6t) - 999999t) / 999998000001
    return (96017284 * (-1999999 + pow(10, 12+6*t, M) - 999999*t)) % M

